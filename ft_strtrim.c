#include "libft.h"

/**
 * The function `ft_strtrim` trims the leading and trailing characters from a string that match any
 * characters in a given set.
 * 
 * @param s1 The parameter `s1` is a pointer to a constant character string that represents the input
 * string from which we want to remove leading and trailing characters.
 * @param set The `set` parameter is a string that contains a set of characters. These characters are
 * considered to be the leading and trailing characters that need to be trimmed from the `s1` string.
 * 
 * @return The function `ft_strtrim` returns a newly allocated string that is a trimmed version of the
 * input string `s1`.
 */
int	ft_getstart(const char *s1, const char *set)
{
	size_t	len;
	size_t	i;

	len = ft_strlen(s1);
	i = 0;
	while (i < len)
	{
		if (ft_strchr(set, s1[i]) == 0)
			break ;
		i++;
	}
	return (i);
}

int	ft_getend(const char *s1, const char *set)
{
	size_t	len;
	size_t	i;

	len = ft_strlen(s1);
	i = 0;
	while (i < len)
	{
		if (ft_strchr(set, s1[len - i - 1]) == 0)
			break ;
		i++;
	}
	return (len - i);
}

char	*ft_strtrim(char const *s1, char const *set)
{
	int		start;
	int		end;
	char	*newstr;

	if (s1 == NULL)
		return (NULL);
	if (set == NULL)
		return (ft_strdup(s1));
	start = ft_getstart(s1, set);
	end = ft_getend(s1, set);
	if (start >= end)
		return (ft_strdup(""));
	newstr = (char *)malloc(sizeof(char) * (end - start + 1));
	if (newstr == NULL)
		return (NULL);
	ft_strlcpy(newstr, s1 + start, end - start + 1);
	return (newstr);
}
